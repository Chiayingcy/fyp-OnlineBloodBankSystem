    {
        $appointment = Appointment::find($id);
        $appointment->appointmentStatus = $request->status;
        $appointment->reason = $request->reason;
        $appointment->save();

        return redirect()->route('Hospitals.viewDonorAppointment')->with('message', __('Blood Request update is successfully.'));
    }

